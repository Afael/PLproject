    private void isiComboBox() {
        cmbPemasok.removeAllItems();
        try {
            koneksi = MySQLConn.getKoneksi();
            state = koneksi.createStatement();
            String query = "SELECT * FROM pemasok";

            rs = state.executeQuery(query);
            while (rs.next()) {

                String sNamaPemasok = rs.getString("nama_pemasok");
                cmbPemasok.addItem(sNamaPemasok);
            }
            rs.close();
            state.close();
        } catch (Exception ex) {
            System.out.println(ex);
        }
    }

