    private void tblMWHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMWHMouseClicked
        CtrInputMWH cm = new CtrInputMWH();
        if (evt.getClickCount() == 1) {
            JTable target = (JTable) evt.getSource();
            int row = target.getSelectedRow();
            setEditState(false);
            txtNoMWH.setText(target.getModel().getValueAt(row, 0).toString());
            txtNoForm.setText(target.getModel().getValueAt(row, 1).toString());
            String sIdPemasok = target.getModel().getValueAt(row, 2).toString();
            //int inIdPemasok = Integer.valueOf(sIdPemasok);
            //String namaPemasok = cm.getNamaPemasok(inIdPemasok);
            cmbPemasok.setSelectedItem(sIdPemasok);
            txtNoSuratJalan.setText(target.getModel().getValueAt(row, 3).toString());
            try {
                java.util.Date sDate = new SimpleDateFormat("yyy-MM-dd").parse(target.getModel().getValueAt(row, 4).toString());
                jxdTglMWH.setDate(sDate);
            } catch (ParseException ex) {
                Logger.getLogger(MDMWH.class.getName()).log(Level.SEVERE, null, ex);
            }
            txtWarna.setText(target.getModel().getValueAt(row, 5).toString());
            txtJmlPengiriman.setText(target.getModel().getValueAt(row, 6).toString());
            txtJmlTest.setText(target.getModel().getValueAt(row, 7).toString());
            txtJenisBarang.setText(target.getModel().getValueAt(row, 8).toString());
            txtUkuran.setText(target.getModel().getValueAt(row, 9).toString());
        }
    }//GEN-LAST:event_tblMWHMouseClicked

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        CtrInputMWH cm = new CtrInputMWH();
        String sNoMWH = txtNoMWH.getText();
        String sNoForm = txtNoForm.getText();
        String namaPemasok = cmbPemasok.getSelectedItem().toString();
        String sNoSuratJalan = txtNoSuratJalan.getText();
        String sWarna = txtWarna.getText();
        String sJumlahPengiriman = txtJmlPengiriman.getText();
        String sJumlahdiTest = txtJmlTest.getText();
        String sJenisBarang = txtJenisBarang.getText();
        String sUkuran = txtUkuran.getText();
        Date sTanggal = new java.sql.Date(jxdTglMWH.getDate().getTime());
        if (sNoMWH.trim().equals("") || sNoForm.trim().equals("") || sNoSuratJalan.trim().equals("")
                || sWarna.trim().equals("") || sJumlahPengiriman.trim().equals("") || sJumlahdiTest.trim().equals("")
                || sJenisBarang.trim().equals("") || sUkuran.trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Please review you entry!");
        } else {
            int idPemasok = cm.getIdPemasok(namaPemasok);
            simpanMWH(sNoMWH, sNoForm, idPemasok, sNoSuratJalan, sWarna, sJumlahPengiriman, sJumlahdiTest, sJenisBarang, sUkuran, sTanggal);
            tblMWH.setModel(cm.getModelInputMWH());
            setEditState(false);
            cleartextfield();
            JOptionPane.showMessageDialog(this, "Add data successfull");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

