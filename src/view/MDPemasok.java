    private void autoNumber() {
        try {
            koneksi = MySQLConn.getKoneksi();
            state = koneksi.createStatement();
            String query = "SELECT COUNT(id_pemasok) AS tot FROM pemasok;";

            rs = state.executeQuery(query);
            while (rs.next()) {
                if (rs.first() == false) {
                    IdPemasokTextField.setText("1");
                } else {
                    rs.last();
                    int nilai = rs.getInt(1) + 1;
                    String no = String.valueOf(nilai);
                    /*int noLong = no.length();
                     for (int a = 0; a < 2 - noLong; a++) {
                     no += no;
                     }*/
                    IdPemasokTextField.setText(no);
                }

            }
            rs.close();
            state.close();
        } catch (Exception ex) {
            System.out.println(ex);
        }
    }

