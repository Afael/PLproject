    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        CtrInputPemasok cp = new CtrInputPemasok();
        String sIdPemasok = IdPemasokTextField.getText();
        String sNama = txtNamaPemasok.getText();
        String sAlamat = txtAlamat.getText();
        String sPhone = txtPhone.getText();
        try {
            MySQLConn con = new MySQLConn();
            PreparedStatement stm = con.connect("SELECT * FROM pemasok WHERE id_pemasok = ?");
            stm.setString(1, sIdPemasok);
            ResultSet rs = stm.executeQuery();

            if (rs.next()) {
                cp.setId_pemasok(Integer.parseInt(IdPemasokTextField.getText()));
                cp.setNama_pemasok(txtNamaPemasok.getText());
                cp.setAlamat(txtAlamat.getText());
                cp.setPhone(txtPhone.getText());
                cp.ubahPemasok();
                tblPemasok.setModel(cp.getModelInputPemasok());
                cleartextfield();
                autoNumber();
                JOptionPane.showMessageDialog(this, "Update data successfull");
            } else if (sNama.trim().equals("") || sAlamat.trim().equals("") || sPhone.trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Please review you entry!");
            } else {
                simpanPemasok(sNama, sAlamat, sPhone);
                tblPemasok.setModel(cp.getModelInputPemasok());
                cleartextfield();
                autoNumber();
                JOptionPane.showMessageDialog(this, "Add data successfull");
            }
            rs.close();
            state.close();
        } catch (Exception ex) {
            System.out.println(ex);
        }

    }//GEN-LAST:event_btnSimpanActionPerformed

